public class City {

    private int city_id;
    private String ciy_name;

    public City(int city_id, String ciy_name) {
        this.city_id = city_id;
        this.ciy_name = ciy_name;
    }

    public int getCity_id() {
        return city_id;
    }

    public String getCiy_name() {
        return ciy_name;
    }
}
